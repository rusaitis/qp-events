class TestAgainstFortran:
    """Compare Python KMAG output against Fortran reference values."""

    @pytest.fixture
    def field(self):
        return SaturnField()

    @pytest.mark.parametrize(
        "row_idx",
        range(len(_FORTRAN_REF)),
        ids=[
            "10Rs_eq_phi0",
            "10Rs_eq_phipi2",
            "10Rs_eq_phipi",
            "5Rs_eq_phi0",
            "15Rs_eq_phi0",
            "20Rs_eq_phi0",
            "10Rs_45lat",
            "5Rs_pole",
            "3Rs_eq",
            "10Rs_eq_t0",
            "10Rs_th60_phi45",
        ],
    )
    def test_field_matches_fortran(self, field, row_idx):
        """Python field_s3c should match Fortran KMAG within 0.1%."""
        row = _FORTRAN_REF[row_idx]
        r, theta, phi, time = row[0], row[1], row[2], row[3]
        br_ref, bt_ref, bp_ref = row[4], row[5], row[6]

        br, bt, bp = field.field_s3c(r, theta, phi, time)

        # Use relative tolerance for components > 1 nT,
        # absolute tolerance for small components
        b_mag = math.sqrt(br_ref**2 + bt_ref**2 + bp_ref**2)

        assert_allclose(
            br,
            br_ref,
            rtol=1e-3,
            atol=0.01 * b_mag,
            err_msg=f"Br mismatch at r={r}, theta={theta:.3f}",
        )
        assert_allclose(
            bt,
            bt_ref,
            rtol=1e-3,
            atol=0.01 * b_mag,
            err_msg=f"Bt mismatch at r={r}, theta={theta:.3f}",
        )
        assert_allclose(
            bp,
            bp_ref,
            rtol=1e-3,
            atol=0.01 * b_mag,
            err_msg=f"Bp mismatch at r={r}, theta={theta:.3f}",
        )


