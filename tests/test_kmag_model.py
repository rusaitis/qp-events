class TestKnownLimits:
    """Tests for physically expected limiting behavior."""

    def test_dipole_dominates_at_small_r(self):
        """At r=2 Rs, the total field should be dominated by the dipole (~2600 nT)."""
        field = SaturnField()
        _, bt, _ = field.field_s3c(2.0, math.pi / 2, 0.0, time=284040000.0)
        # Dipole B0z=21191, so at equator r=2: Bt ~ B0z/r^3 = 21191/8 ~ 2649 nT
        assert abs(bt) > 2000.0
        assert abs(bt) < 4000.0

    def test_field_weak_at_large_r(self):
        """At 25 Rs equator, total field should be < 5 nT."""
        field = SaturnField()
        br, bt, bp = field.field_s3c(25.0, math.pi / 2, 0.0, time=284040000.0)
        b_mag = math.sqrt(br**2 + bt**2 + bp**2)
        assert b_mag < 5.0

    def test_outside_magnetopause_only_imf(self):
        """Far outside the magnetopause, field should equal IMF penetration only."""
        # At r=100 Rs subsolar, well outside MP (~24 Rs)
        field = SaturnField()
        br, bt, bp = field.field_s3c(100.0, math.pi / 2, 0.0, time=284040000.0)
        b_mag = math.sqrt(br**2 + bt**2 + bp**2)
        # IMF penetration is very weak (~0.1 nT with default parameters)
        assert b_mag < 1.0

    def test_field_continuous_across_equator(self):
        """Field should be continuous across the equatorial plane."""
        field = SaturnField()
        t = 284040000.0
        # Just above and below equator
        _, bt_above, _ = field.field_s3c(10.0, math.pi / 2 - 0.01, 0.0, t)
        _, bt_below, _ = field.field_s3c(10.0, math.pi / 2 + 0.01, 0.0, t)
        # Should be very close (current sheet might cause some asymmetry)
        assert abs(bt_above - bt_below) / max(abs(bt_above), 0.1) < 0.1

    def test_axisymmetry_of_internal_field(self):
        """Internal field should be nearly axisymmetric (Saturn's H coefficients are zero)."""
        field = SaturnField()
        g, h, rec = field._g, field._h, field._rec
        results = []
        for phi in [0.0, math.pi / 2, math.pi, 3 * math.pi / 2]:
            br, bt, bp = internal_field_jit(5.0, math.pi / 3, phi, g, h, rec, 3)
            results.append((br, bt))
        br_vals = [r[0] for r in results]
        bt_vals = [r[1] for r in results]
        assert_allclose(br_vals, br_vals[0], atol=1e-10)
        assert_allclose(bt_vals, bt_vals[0], atol=1e-10)


