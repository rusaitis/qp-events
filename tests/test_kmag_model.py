class TestCoordinateTransforms:
    """Tests for saturn_coords.py coordinate transformations."""

    def test_s3c_roundtrip(self):
        """S3C → KSM → S3C should be identity."""
        v = np.array([1.0, 2.0, 3.0])
        t = 284040000.0
        v_ksm = rotate_vector(v, "S3C", "KSM", t)
        v_back = rotate_vector(v_ksm, "KSM", "S3C", t)
        assert_allclose(v_back, v, atol=1e-12)

    def test_dis_roundtrip(self):
        """S3C → DIS → S3C should be identity."""
        v = np.array([5.0, -3.0, 7.0])
        t = 284040000.0
        v_dis = rotate_vector(v, "S3C", "DIS", t)
        v_back = rotate_vector(v_dis, "DIS", "S3C", t)
        assert_allclose(v_back, v, atol=1e-12)

    def test_kso_roundtrip(self):
        """S3C → KSO → S3C should be approximately identity.

        KSO axes aren't perfectly orthogonal (Zkso is independent of sun
        direction), so roundtrip has ~0.03% error. This matches the Fortran.
        """
        v = np.array([-2.0, 4.0, 1.0])
        t = 100000000.0
        v_kso = rotate_vector(v, "S3C", "KSO", t)
        v_back = rotate_vector(v_kso, "KSO", "S3C", t)
        assert_allclose(v_back, v, atol=1e-3)

    def test_sun_position_returns_finite(self):
        """sun_position should return finite values for valid times."""
        stheta, sphi, ztheta, zphi = sun_position(284040000.0)
        assert math.isfinite(stheta)
        assert math.isfinite(sphi)
        assert math.isfinite(ztheta)
        assert math.isfinite(zphi)

    def test_epoch_j2000_passthrough(self):
        """j2000 epoch should return time unchanged."""
        assert epoch_to_j2000(12345.0, "j2000") == 12345.0


