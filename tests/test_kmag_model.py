class TestInternalField:
    """Tests for the spherical harmonic internal field."""

    @pytest.fixture(scope="class")
    def coeffs(self):
        """Get normalized coefficients from a SaturnField instance."""
        field = SaturnField()
        return field._g, field._h, field._rec

    def test_equatorial_field_finite(self, coeffs):
        """Internal field at equator should be finite and small at large r."""
        g, h, rec = coeffs
        br, bt, bp = internal_field_jit(10.0, math.pi / 2, 0.0, g, h, rec, 3)
        assert all(math.isfinite(x) for x in (br, bt, bp))
        assert abs(bt) < 1.0

    def test_field_decreases_with_distance(self, coeffs):
        """Internal field should decrease with distance."""
        g, h, rec = coeffs
        _, bt_near, _ = internal_field_jit(3.0, math.pi / 2, 0.0, g, h, rec, 3)
        _, bt_far, _ = internal_field_jit(10.0, math.pi / 2, 0.0, g, h, rec, 3)
        assert abs(bt_near) > abs(bt_far)

    def test_polar_field_stronger(self, coeffs):
        """At the pole, Br should be stronger than at equator."""
        g, h, rec = coeffs
        br_pole, _, _ = internal_field_jit(5.0, 0.1, 0.0, g, h, rec, 3)
        br_eq, _, _ = internal_field_jit(5.0, math.pi / 2, 0.0, g, h, rec, 3)
        assert abs(br_pole) > abs(br_eq)


