class TestExpandedFortranGrid:
    """Compare Python against 54 Fortran points covering Dp, IMF, near-MP, and edge cases."""

    @pytest.fixture(scope="class")
    def expanded_data(self):
        """Load expanded reference (10 cols: r, θ, φ, time, Dp, By, Bz, Br, Bt, Bp)."""
        path = _TESTS_DIR / "fortran_reference_params.txt"
        if not path.exists():
            pytest.skip("fortran_reference_params.txt not found")
        return np.loadtxt(path)

    def test_expanded_grid_loaded(self, expanded_data):
        """Expanded reference should have 54 rows and 10 columns."""
        assert expanded_data.shape[0] >= 50
        assert expanded_data.shape[1] == 10

    def test_all_expanded_points_match(self, expanded_data):
        """All expanded grid points should match Fortran within tolerance."""
        mismatches = []
        for i, row in enumerate(expanded_data):
            r, theta, phi, t = row[0], row[1], row[2], row[3]
            dp, by_imf, bz_imf = row[4], row[5], row[6]
            br_ref, bt_ref, bp_ref = row[7], row[8], row[9]
            b_mag = math.sqrt(br_ref**2 + bt_ref**2 + bp_ref**2)

            config = SaturnFieldConfig(dp=dp, by_imf=by_imf, bz_imf=bz_imf)
            field = SaturnField(config)
            br, bt, bp = field.field_s3c(r, theta, phi, t)

            tol = max(0.001 * b_mag, 0.1)
            if (
                abs(br - br_ref) > tol
                or abs(bt - bt_ref) > tol
                or abs(bp - bp_ref) > tol
            ):
                mismatches.append(
                    f"Row {i}: r={r}, Dp={dp}, By={by_imf}, Bz={bz_imf} "
                    f"Br: {br:.4f} vs {br_ref:.4f}, "
                    f"Bt: {bt:.4f} vs {bt_ref:.4f}"
                )
        assert not mismatches, (
            f"{len(mismatches)}/{len(expanded_data)} points failed:\n"
            + "\n".join(mismatches[:10])
        )


class TestFortranGrid:
    """Compare Python against 229 Fortran reference points covering r, theta, phi, time."""

    @pytest.fixture(scope="class")
    def grid_data(self):
        """Load the Fortran reference grid (7 columns: r, theta, phi, time, Br, Bt, Bp)."""
        path = _TESTS_DIR / "fortran_reference_grid.txt"
        if not path.exists():
            pytest.skip(
                "fortran_reference_grid.txt not found — run fortran/kmag_grid_driver"
            )
        return np.loadtxt(path)

    @pytest.fixture(scope="class")
    def field(self):
        return SaturnField()

    def test_grid_loaded(self, grid_data):
        """Reference grid should have 229 rows and 7 columns."""
        assert grid_data.shape[0] >= 200
        assert grid_data.shape[1] == 7

    def test_all_grid_points_match(self, field, grid_data):
        """All 229 grid points should match Fortran within 0.1% or 0.1 nT."""
        mismatches = []
        for i, row in enumerate(grid_data):
            r, theta, phi, t = row[0], row[1], row[2], row[3]
            br_ref, bt_ref, bp_ref = row[4], row[5], row[6]
            b_mag = math.sqrt(br_ref**2 + bt_ref**2 + bp_ref**2)

            br, bt, bp = field.field_s3c(r, theta, phi, t)

            tol = max(0.001 * b_mag, 0.1)  # 0.1% or 0.1 nT
            if (
                abs(br - br_ref) > tol
                or abs(bt - bt_ref) > tol
                or abs(bp - bp_ref) > tol
            ):
                mismatches.append(
                    f"Row {i}: r={r}, th={theta:.3f}, ph={phi:.3f} "
                    f"Br: {br:.4f} vs {br_ref:.4f}, "
                    f"Bt: {bt:.4f} vs {bt_ref:.4f}, "
                    f"Bp: {bp:.4f} vs {bp_ref:.4f}"
                )
        assert not mismatches, (
            f"{len(mismatches)}/{len(grid_data)} points failed:\n"
            + "\n".join(mismatches[:10])
        )

    def test_grid_relative_error_statistics(self, field, grid_data):
        """Median relative error across all grid points should be < 0.01%."""
        errors = []
        for row in grid_data:
            r, theta, phi, t = row[0], row[1], row[2], row[3]
            b_ref = np.array([row[4], row[5], row[6]])
            b_mag = np.linalg.norm(b_ref)
            if b_mag < 0.01:
                continue

            br, bt, bp = field.field_s3c(r, theta, phi, t)
            b_py = np.array([br, bt, bp])
            rel_err = np.linalg.norm(b_py - b_ref) / b_mag
            errors.append(rel_err)

        errors = np.array(errors)
        median_err = np.median(errors)
        max_err = np.max(errors)
        assert median_err < 1e-4, (
            f"Median relative error {median_err:.2e} exceeds 0.01%"
        )
        assert max_err < 0.01, f"Max relative error {max_err:.2e} exceeds 1%"


